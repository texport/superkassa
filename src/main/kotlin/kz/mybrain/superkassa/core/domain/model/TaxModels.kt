package kz.mybrain.superkassa.core.domain.model

import kotlinx.serialization.Serializable

/**
 * Налоговый режим ККМ.
 *
 * NO_VAT   - касса не является плательщиком НДС (только без НДС / освобожденный оборот).
 * VAT_PAYER - касса является плательщиком НДС (доступны ставки 0% и 16%).
 * MIXED    - смешанный режим (на будущее, для касс с разным типом оборотов).
 */
@Serializable
enum class TaxRegime {
    NO_VAT,
    VAT_PAYER,
    MIXED
}

/**
 * Налоговые группы НДС, поддерживаемые ККМ.
 *
 * NO_VAT - «Без НДС» (освобожденный оборот, операции вне системы НДС).
 * VAT_0  - «НДС 0%» (облагаемый оборот по нулевой ставке).
 * VAT_5  - «НДС 5%» (льготные операции, например, ряд медуслуг и лекарств с 2026 года).
 * VAT_10 - «НДС 10%» (льготные операции, включая часть печатных изданий и медуслуг).
 * VAT_16 - «НДС 16%» (базовая ставка согласно НК РК с 2026 года).
 */
@Serializable
enum class VatGroup {
    NO_VAT,
    VAT_0,
    VAT_5,
    VAT_10,
    VAT_16
}

